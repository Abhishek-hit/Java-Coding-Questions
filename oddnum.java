public class oddnum {
    public static void main(String[] args) {
        for (int i=0;i<=100; i++)
            if (i%2==0){
                System.out.println ("the even number is "+i );
            }

        for (int i=0;i<=100; i++)
            if (i%2!=0){
                System.out.println ("the odd number is "+i );
            }
    }

}
